package JavaStart.Lesson02;

public class ArithmeticOperations {

    public static void main(String[] args) {
        int age = 2_000_000_000;

        System.out.println(age + 10);
        System.out.println(age - 10);
        System.out.println(age * 10);
        System.out.println(age / 10);
        System.out.println(age % 10);
    }
}
